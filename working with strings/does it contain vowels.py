""" Please write a program which asks the user to input a string. The program then prints out different messages if the string contains any of the vowels a, e or o. """
word = input("Word: ")
if 'a' in word:
    print("a found")
else:
    print("a not found")
if 'e' in word:
    print("e found")
else:
    print("e not found")
if 'o' in word:
    print("o found")
else:
    print("o not found")